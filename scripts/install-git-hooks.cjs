#!/usr/bin/env node
const fs = require("node:fs");
const path = require("node:path");

const root = process.cwd();
const hooksDir = path.join(root, ".git", "hooks");
const preCommitPath = path.join(hooksDir, "pre-commit");
const postCommitPath = path.join(hooksDir, "post-commit");
const prePushPath = path.join(hooksDir, "pre-push");
const checkVersionScript = "E:/Dev/Tool/scripts/check-version-sync.mjs";

if (!fs.existsSync(hooksDir)) {
  console.error("[hooks] .git/hooks not found. Run this from the repository root.");
  process.exit(1);
}

const preCommitHook = [
  "#!/bin/sh",
  "# Auto-generated by scripts/install-git-hooks.cjs",
  "if [ \"$TOOL_HUB_SKIP_VERSION_HOOK\" = \"1\" ]; then",
  "  exit 0",
  "fi",
  "node scripts/git-pre-commit-version.cjs",
  "",
].join("\n");

const postCommitHook = [
  "#!/bin/sh",
  "# Auto-generated by scripts/install-git-hooks.cjs",
  "if [ \"$TOOL_HUB_SKIP_VERSION_HOOK\" = \"1\" ]; then",
  "  exit 0",
  "fi",
  "node scripts/git-post-commit-changelog.cjs",
  "",
].join("\n");

fs.writeFileSync(preCommitPath, preCommitHook, "utf8");
fs.chmodSync(preCommitPath, 0o755);
console.log(`[hooks] Installed pre-commit version hook: ${preCommitPath}`);

fs.writeFileSync(postCommitPath, postCommitHook, "utf8");
fs.chmodSync(postCommitPath, 0o755);
console.log(`[hooks] Installed post-commit changelog hook: ${postCommitPath}`);

const prePushHook = [
  "#!/bin/sh",
  "# Auto-generated by scripts/install-git-hooks.cjs",
  'if [ "$TOOL_HUB_SKIP_VERSION_HOOK" = "1" ]; then',
  "  exit 0",
  "fi",
  `node "${checkVersionScript.replace(/\\/g, "/")}" --product-root "$(git rev-parse --show-toplevel)" --no-warn-published`,
  "",
].join("\n");

fs.writeFileSync(prePushPath, prePushHook, "utf8");
fs.chmodSync(prePushPath, 0o755);
console.log(`[hooks] Installed pre-push version sync hook: ${prePushPath}`);
